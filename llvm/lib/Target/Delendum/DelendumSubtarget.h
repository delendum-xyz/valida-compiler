#ifndef DELENDUMSUBTARGET_H
#define DELENDUMSUBTARGET_H

#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/MC/MCInstrItineraries.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "DelendumGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class DelendumSubtarget : public DelendumGenSubtargetInfo {
  virtual void anchor();

public:
  // NOTE: O64 will not be supported.
  enum DelendumABIEnum {
    UnknownABI, O32
  };

protected:
  enum DelendumArchEnum {
    Delendum32
  };

  // Delendum architecture version
  DelendumArchEnum DelendumArchVersion;

  // Delendum supported ABIs
  DelendumABIEnum DelendumABI;

  // IsLittle - The target is Little Endian
  bool IsLittle;

  // UseSmallSection - Small section is used.
  bool UseSmallSection;

  InstrItineraryData InstrItins;

  // Relocation Model
  Reloc::Model RM;

public:
  unsigned getTargetABI() const { return DelendumABI; }

  /// This constructor initializes the data members to match that
  /// of the specified triple.
  DelendumSubtarget(const std::string &TT, const std::string &CPU,
                const std::string &FS, bool little, Reloc::Model _RM);

//- Vitual function, must have
  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  bool isLittle() const { return IsLittle; }
  bool useSmallSection() const { return UseSmallSection; }
};
} // End llvm namespace

#endif
