#ifndef VALIDASUBTARGET_H
#define VALIDASUBTARGET_H

#include "ValidaInstrInfo.h"
#include "ValidaISelLowering.h"
#include "ValidaFrameLowering.h"

#include "llvm/CodeGen/GlobalISel/CallLowering.h"
#include "llvm/CodeGen/GlobalISel/LegalizerInfo.h"
#include "llvm/CodeGen/GlobalISel/InstructionSelector.h"
#include "llvm/CodeGen/RegisterBankInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/MC/MCInstrItineraries.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "ValidaGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class ValidaSubtarget : public ValidaGenSubtargetInfo {
  virtual void anchor();

  ValidaInstrInfo InstrInfo;
  ValidaTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
  ValidaFrameLowering FrameLowering;

  const ValidaTargetMachine &TM;

public:
  ValidaSubtarget(const Triple &TT, StringRef CPU,
                    StringRef FS, const ValidaTargetMachine &TM);

  const ValidaInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const ValidaRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const ValidaTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  Align getStackAlignment() const { return Align(4); }

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);
  ValidaSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

protected:
  // GlobalISel related APIs.
  std::unique_ptr<CallLowering> CallLoweringInfo;
  std::unique_ptr<LegalizerInfo> Legalizer;
  std::unique_ptr<RegisterBankInfo> RegBankInfo;
  std::unique_ptr<InstructionSelector> InstSelector;

public:
  const CallLowering *getCallLowering() const override;
  const LegalizerInfo *getLegalizerInfo() const override;
  const RegisterBankInfo *getRegBankInfo() const override;
  InstructionSelector *getInstructionSelector() const override;

};
} // End llvm namespace

#endif
